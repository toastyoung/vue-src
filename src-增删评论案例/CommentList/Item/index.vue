<template>
  <li class="list-group-item">
    <div class="handle">
      <a href="javascript:;" @click="del">删除</a>
    </div>
    <p class="user">
      <span>{{ comment.name }}</span
      ><span>说:</span>
    </p>
    <p class="centence">{{ comment.content }}</p>
  </li>
</template>
<script>
export default {
  name: "Item",
  props: {
    comment: Object,
    delComment: Function,
  },
  methods: {
    del() {
      const { id, name } = this.comment;
      if (confirm(`确认删除${name}吗`)) {
        this.delComment(id);
      }
    },
  },
};
</script>

